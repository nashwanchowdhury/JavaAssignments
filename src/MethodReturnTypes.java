
public class MethodReturnTypes {
    public static void main(String[] args) {
        String("Hi");
        Integer(5);
        Boolean(true);
    }
    public static void String(String x) {
        System.out.println(x);

    }
    public static void Integer(int x) {
        System.out.println(x);
    }
    public static void Boolean(boolean x) {
        System.out.println(x);
    }
}
